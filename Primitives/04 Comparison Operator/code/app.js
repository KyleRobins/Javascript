// RELATIONAL OPERATORS
// > Greator than
// < Less than
// >= Greator than or equal to
// <= Less than or equal to

let greatorVal = 12;
let lessVal = 7;

console.log(greatorVal > lessVal);
console.log(greatorVal < lessVal);
console.log(greatorVal >= lessVal);
console.log(greatorVal <= lessVal);

// Equality Operators

// === Strict Equality (type + value)
// !== Strict non-Equality operators (type + value)
// == Loose Equality operator (value)
// != Loose Not Equality operator (value)

console.log(10 === "10"); // Strict Equality
console.log(10 !== 10); // Non-Equality
console.log(10 == "10"); // Loose Equality just check the Value
console.log(10 != 10); // Loose Not Equality 


