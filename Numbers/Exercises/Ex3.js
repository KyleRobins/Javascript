// 1. Generate a random decimal number between `0` (inclusive) and `1` (exclusive).
// 2. Calculate the square of `9`.
// 3. Find the square root of `64`.


// 1. Generate a random decimal number between `0` (inclusive) and `1` (exclusive)

let randomDecimal = Math.random();
console.log("Random Decimal:", randomDecimal);

// 2. Calculate the square of `9`

//Method one using the Math.pow function
let squareOf9 = Math.pow(9, 2);
console.log("The Square of 9 is:", squareOf9);

//Method two using the exponentiation operator **

squareOf9 = 9 ** 2;
console.log("The Square of 9 is:", squareOf9);

// 3. Find the square root of `64`

let sqaureroot = Math.sqrt(64);
console.log("The Squareroot of 64 is:", sqaureroot);





