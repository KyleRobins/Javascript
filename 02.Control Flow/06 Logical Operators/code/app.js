// 1. --> Logical And (&&) Operator
//  -> True if both the operands/boolean values are true, else evaluates to false
const a = true;
const b = false;
const c = 5;
console.log(a && b);

// 2. --> Logical OR (||) Operator
// -> TRUE if either of the operands/boolean values is true, else evaluates to false if both are false.
console.log(a || b);

// 3. --> Logical Not (!) Operator
// -> TRUE if the operands is false and vice-versa
console.log(!a);
